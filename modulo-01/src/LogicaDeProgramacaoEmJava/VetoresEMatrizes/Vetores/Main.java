package LogicaDeProgramacaoEmJava.VetoresEMatrizes.Vetores;
public class Main {
  public static void main(String[] args) {
    // cria um vetor de inteiros de tamanho 5
    int[] dados = new int[5];
    dados[0] = 6;
    dados[2] = 9;
    dados[3] = 7;
    System.out.println(dados[3]); // Mostra o dado gravado na posição 3
    System.out.println(dados); // Mostra o endereço de memória do vetor
  }
}
